public class PopAttributes {
    int width;
    int height;
    int grarity;
    int leftOffset;

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getGrarity() {
        return grarity;
    }

    public void setGrarity(int grarity) {
        this.grarity = grarity;
    }

    public int getLeftOffset() {
        return leftOffset;
    }

    public void setLeftOffset(int leftOffset) {
        this.leftOffset = leftOffset;
    }
}
